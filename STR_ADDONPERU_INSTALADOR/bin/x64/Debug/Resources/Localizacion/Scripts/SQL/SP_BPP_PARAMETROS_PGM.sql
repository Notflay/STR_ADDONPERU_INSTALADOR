CREATE PROCEDURE SP_BPP_PARAMETROS_PGM
    @VAR NVARCHAR(50),
	@PARAM1 NVARCHAR(50),
	@PARAM2 NVARCHAR(50)
AS
BEGIN

IF(@VAR = 'TIPO_PAGO')
BEGIN

SELECT distinct U_BPP_TIPO Code , Descr Name FROM "@BPP_PGM_TIPPAGO" T1
INNER JOIN UFD1 T2 ON T1.U_BPP_TIPO = T2.FldValue AND T2.TableID = '@BPP_PGM_TIPPAGO'

END

IF(@VAR = 'CUENTA_BANCO')
BEGIN
SELECT FormatCode FROM OACT WHERE AcctCode = @PARAM1
END 


IF(@VAR = 'CUENTA_CONTABLE')
BEGIN
SELECT T2.AcctCode Code,T2.FormatCode Name FROM DSC1 T1 INNER JOIN OACT T2 ON T1.GLAccount = T2.AcctCode 
END 

END
